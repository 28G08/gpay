    private void beli() {
        try {
            res = stat.executeQuery("select * from saldo where username = '" + txtusernametp.getText() + "' "
                    + "and password = '" + txtpwtp.getText() + "';");
            if (res.next()) {
                if (txtusernametp.getText().equals(res.getString("username")) && txtpwtp.getText().equals(res.getString("password"))) {
                    String a = txtnomtp.getText();
                    int saldokurang = Integer.parseInt(a);
                    res = stat.executeQuery("select saldo from saldo where username = '" + txtusernametp.getText() + "' "
                            + "and password = '" + txtpwtp.getText() + "';");
                    res.next();
                    String str = res.getString("saldo");
                    int saldoakhir = Integer.parseInt(str);
                    int saldobaru = saldoakhir - saldokurang;

                    res = stat.executeQuery("select nama from account where username = '" + txtusernametp.getText() + "' "
                            + "and password = '" + txtpwtp.getText() + "';");
                    res.next();
                    String namapembeli = res.getString("nama");

                    res = stat.executeQuery("select saldo from saldo where username = '" + user + "';");
                    res.next();
                    String string = res.getString("saldo");
                    int saldoakhirsektor = Integer.parseInt(string);
                    int saldobarusektor = saldoakhirsektor + saldokurang;

                    res = stat.executeQuery("select password from saldo where username = '" + user + "';");
                    res.next();
                    String passwordsektor = res.getString("password");

                    if (saldokurang < saldoakhir) {
                        stat.executeUpdate("update saldo set saldo = '" + saldobaru + "' where username = '" + txtusernametp.getText() + "' "
                                + "and password = '" + txtpwtp.getText() + "';");
                        stat.executeUpdate("update saldo set saldo = '" + saldobarusektor + "' where username = '" + user + "';");

                        stat.executeUpdate("insert into riwayat values ('" + txtusernametp.getText() + "', '"
                                + txtpwtp.getText() + "', '" + saldokurang + "', 'TRANSFER', 'KOPERASI MAHASISWA'), ('" + user + "', '"
                                + passwordsektor + "', '" + saldokurang + "', 'TRANSFER', '" + namapembeli + "');");

                        txtusernametp.setText("");
                        txtpwtp.setText("");
                        txtnomtp.setText("");
                        JOptionPane.showMessageDialog(null, "Pembelian berhasil! :)");
                    } else if (saldokurang > saldoakhir) {
                        try {
                            JOptionPane.showMessageDialog(null, "Sisa Saldo anda = " + saldoakhir + ", mohon top up dulu!", "Saldo Kurang", JOptionPane.WARNING_MESSAGE);
                        } catch (Exception e) {
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "username atau password salah");
                }
            }

        } catch (Exception e) {
        }
    }

