    private void btntpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btntpMouseClicked
        if (txtusernametp.getText().equals("") && txtpwtp.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Mohon isi dulu");
        } else {
            int input = JOptionPane.showConfirmDialog(null, "Benar ingin melakukan pembelian sebesar " + txtnomtp.getText()
                    + " pada akun " + txtusernametp.getText() + "?", "Transfer", JOptionPane.YES_NO_OPTION);
            if (input == 0) {
                batal.setVisible(false);
                clear.setVisible(false);
                beli();
            } else if (input == 1) {
                batal.setVisible(true);
                clear.setVisible(true);
            }
        }
    }//GEN-LAST:event_btntpMouseClicked

