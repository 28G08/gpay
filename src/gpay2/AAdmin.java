    private void tabel() {
        DefaultTableModel tb = new DefaultTableModel();

        tb.addColumn("Nama");
        tb.addColumn("Username");
        tb.addColumn("Password");
        jTable1.setModel(tb);
        try {

            res = stat.executeQuery("select nama, username, password from account where akses = 'ADMIN'");

            while (res.next()) {

                tb.addRow(new Object[]{
                            res.getString("nama"),
                            res.getString("username"),
                            res.getInt("password")});
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "belum terkoneksi :(");
        }
    }

